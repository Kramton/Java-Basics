package JavaBasics01;

public class Access1 
{
	// Four types of access modifiers:
	// default, public, private and protected
	
	// this is an example of a default:
	int hours = 3;
	int minutes = 47;
	
	// this is an example of public
	public int num1 = 6;
	public int num2 = 12;
	
	// private, only accessible in this file.
	private int num3 = 71;
	private int num4 = 53;
	
	// protected, gives access to everything in the same package.
	protected int num5 = 32;
	protected int num6 = 64;
}